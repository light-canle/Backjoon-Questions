    public static void Main(string[] args)
    {
        int t = int.Parse(Console.ReadLine());
        for (int i = 0; i < t; i++)
        {
            int[] input = Array.ConvertAll(Console.ReadLine().Trim().Split(), int.Parse);
            Console.WriteLine("yes");
        }
    }
}
